N=11225
##########################################################################################

dp=[0]*(N+1)
P=lambda n: (n//2)*(3*(n//2)-1)//2 if n%2==0 else (n//2)*(3*(n//2)+1)//2
dp[0]=1
for i in range(1, N+1):
    j=1
    while P(j)<=i:
        dp[i]+=(-1)**(1+j//2) * dp[i-P(j)]
        j+=1
print(dp[N]-1)