D=10**6
##########################################################################################

dp=[1]
P=lambda n: (n//2)*(3*(n//2)-1)//2 if n%2==0 else (n//2)*(3*(n//2)+1)//2
while dp[-1]%D!=0:
    j,t=1,1
    while P(j)<=len(dp):
        t=(t+(-1)**(1+j//2)*dp[-P(j)])%D
        j+=1
    dp.append(t%D)
print(len(dp)-1)
